_gpt_semaphore: Optional[asyncio.Semaphore] = None
_deepseek_semaphore: Optional[asyncio.Semaphore] = None


def _get_semaphores() -> tuple[asyncio.Semaphore, asyncio.Semaphore]:
    """Lazily create semaphores (must be called from within an event loop)."""
    global _gpt_semaphore, _deepseek_semaphore
    if _gpt_semaphore is None:
        _gpt_semaphore = asyncio.Semaphore(ASYNC_CONFIG["gpt_max_concurrency"])
    if _deepseek_semaphore is None:
        _deepseek_semaphore = asyncio.Semaphore(ASYNC_CONFIG["deepseek_max_concurrency"])
    return _gpt_semaphore, _deepseek_semaphore


async def _with_retries(name: str, func, *args, **kwargs) -> Tuple[bool, Any, int, float, Optional[str]]:
    """
    Retry wrapper for synchronous LLM calls run in a thread.

    Returns: (success, result_or_None, retries_used, latency_s, error_msg_or_None)
    """
    cfg = ASYNC_CONFIG
    backoff = cfg["initial_backoff_s"]
    last_err = None

    for attempt in range(cfg["max_retries"] + 1):
        t0 = time.time()
        try:
            result = await asyncio.wait_for(
                asyncio.to_thread(func, *args, **kwargs),
                timeout=cfg["per_call_timeout_s"],
            )
            return True, result, attempt, time.time() - t0, None
        except asyncio.TimeoutError:
            last_err = f"timeout after {cfg['per_call_timeout_s']}s"
        except Exception as e:
            last_err = f"{type(e).__name__}: {e}"

        if attempt < cfg["max_retries"]:
            sleep_time = backoff + random.random() * cfg["jitter_s"]
            await asyncio.sleep(sleep_time)
            backoff *= cfg["backoff_multiplier"]

    return False, None, cfg["max_retries"], time.time() - t0, last_err


async def async_call_gpt(system_msg: str, human_msg: str,
                         temperature: float = 0.0, max_tokens: int = 1024) -> Tuple[bool, str, int, float, Optional[str]]:
    """Async GPT-4o call with semaphore + retries."""
    gpt_sem, _ = _get_semaphores()
    async with gpt_sem:
        return await _with_retries("gpt", _call_gpt_sync, system_msg, human_msg,
                                   temperature, max_tokens)


async def async_call_deepseek(system_msg: str, human_msg: str,
                              temperature: float = 0.0, max_tokens: int = 1024) -> Tuple[bool, str, int, float, Optional[str]]:
    """Async DeepSeek-R1 call with semaphore + retries."""
    _, ds_sem = _get_semaphores()
    async with ds_sem:
        return await _with_retries("deepseek", _call_deepseek_sync, system_msg, human_msg,
                                   temperature, max_tokens)


async def run_async_tasks(coroutines: list, desc: str = "Processing") -> list:
    """
    Run a list of coroutines concurrently with a tqdm progress bar.
    Returns results in the same order as the input coroutines.

    Usage:
        tasks = [async_call_gpt(sys, msg) for sys, msg in prompts]
        results = await run_async_tasks(tasks, desc="Calling GPT-4o")
    """
    tasks = [asyncio.create_task(c) for c in coroutines]
    results = [None] * len(tasks)

    with tqdm(total=len(tasks), desc=desc) as pbar:
        done_count = 0
        pending = set(tasks)
        while pending:
            done, pending = await asyncio.wait(pending, return_when=asyncio.FIRST_COMPLETED)
            for t in done:
                idx = tasks.index(t)
                results[idx] = t.result()
                pbar.update(1)

    return results


