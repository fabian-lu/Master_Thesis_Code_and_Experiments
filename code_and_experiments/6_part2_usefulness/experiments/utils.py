def compute_shap_values(model, test_df: pd.DataFrame) -> tuple[np.ndarray, float]:
    """
    Compute SHAP values for all test instances.

    Returns:
        shap_values: array of shape (n_instances, n_features)
        base_value: float, expected model output
    """
    X_test = test_df[FEATURES].values
    explainer = shap.TreeExplainer(model)
    shap_vals = explainer.shap_values(X_test)
    if isinstance(shap_vals, list):
        shap_vals = shap_vals[0]
    base_value = explainer.expected_value
    if isinstance(base_value, (list, np.ndarray)):
        base_value = float(base_value[0])
    return shap_vals, base_value


def build_test_with_shap(model=None) -> pd.DataFrame:
    """
    Build a DataFrame of all test instances enriched with XGBoost predictions
    and SHAP values per feature.
    """
    test_df = load_test_data()
    if model is None:
        model = load_xgb_model()

    X_test = test_df[FEATURES].values
    predictions = model.predict(X_test)
    shap_vals, base_value = compute_shap_values(model, test_df)

    df = test_df.copy()
    df["prediction"] = predictions
    df["abs_error"] = np.abs(df["True_Value"] - df["prediction"])
    df["pct_error"] = (df["prediction"] - df["True_Value"]) / df["True_Value"] * 100
    df["shap_base_value"] = base_value
    for i, feat in enumerate(FEATURES):
        df[f"shap_{feat}"] = shap_vals[:, i]
    return df


def get_top_k_shap_features(shap_row: pd.Series, k: int = 3) -> list[str]:
    """Return top-k feature names by absolute SHAP value for a single row."""
    shap_cols = [f"shap_{f}" for f in FEATURES]
    vals = shap_row[shap_cols].astype(float).abs()
    top = vals.nlargest(k).index.tolist()
    return [c.replace("shap_", "") for c in top]


