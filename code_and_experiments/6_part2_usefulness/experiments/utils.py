def _call_gpt_sync(system_msg: str, human_msg: str,
                   temperature: float = 0.0, max_tokens: int = 1024) -> dict:
    """
    Synchronous GPT-4o call via Azure OpenAI.
    Returns dict: {text, prompt_tokens, completion_tokens, total_tokens}
    """
    cfg = CONFIG["GPT_CONFIG"]
    client = openai.AzureOpenAI(
        azure_endpoint=cfg["azure_endpoint"],
        api_key=cfg["api_key"],
        api_version=cfg["api_version"],
    )
    response = client.chat.completions.create(
        model=cfg["deployment_name"],
        messages=[
            {"role": "system", "content": system_msg},
            {"role": "user", "content": human_msg},
        ],
        temperature=temperature,
        max_tokens=max_tokens,
    )
    text = response.choices[0].message.content.strip()
    usage = response.usage or {}
    return {
        "text":              text,
        "thought":           "",                              # GPT has no reasoning trace
        "prompt_tokens":     getattr(usage, "prompt_tokens",     count_tokens(system_msg + human_msg)),
        "completion_tokens": getattr(usage, "completion_tokens", count_tokens(text)),
        "total_tokens":      getattr(usage, "total_tokens",      count_tokens(system_msg + human_msg + text)),
    }


def _call_deepseek_sync(system_msg: str, human_msg: str,
                        temperature: float = 0.0, max_tokens: int = 1024) -> dict:
    """
    Synchronous DeepSeek-R1 call via OpenRouter.
    Returns dict: {text, tokens_thought, tokens_answer, prompt_tokens, completion_tokens, total_tokens}

    OpenRouter exposes reasoning in two ways (both handled):
      1. message.reasoning field (preferred by OpenRouter — clean separation)
      2. <think>...</think> tags embedded in message.content (fallback)
    """
    cfg = CONFIG["DEEPSEEK_CONFIG"]
    client = openai.OpenAI(
        base_url=cfg["base_url"],
        api_key=cfg["api_key"],
    )
    response = client.chat.completions.create(
        model=cfg["model"],
        messages=[
            {"role": "system", "content": system_msg},
            {"role": "user", "content": human_msg},
        ],
        temperature=temperature,
        max_tokens=max_tokens,
    )
    msg = response.choices[0].message

    # OpenRouter: reasoning is in message.reasoning (separate field), content is clean answer
    answer  = (msg.content or "").strip()
    thought = (getattr(msg, "reasoning", None) or "").strip()

    usage = response.usage or {}
    return {
        "text":              answer,
        "thought":           thought,                         # raw reasoning text (for saving as InternalThought)
        "tokens_thought":    count_tokens(thought),           # tiktoken, consistent with Paper 1
        "tokens_answer":     count_tokens(answer),            # tiktoken, consistent with Paper 1
        "prompt_tokens":     getattr(usage, "prompt_tokens",     count_tokens(system_msg + human_msg)),
        "completion_tokens": getattr(usage, "completion_tokens", count_tokens(answer)),
        "total_tokens":      getattr(usage, "total_tokens",      count_tokens(system_msg + human_msg + thought + answer)),
    }


