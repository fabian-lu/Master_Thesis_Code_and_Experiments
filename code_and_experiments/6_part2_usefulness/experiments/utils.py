def compute_feature_percentiles(test_df: pd.DataFrame) -> dict:
    """Compute Q25 and Q75 for each feature across the test set."""
    percentiles = {}
    for feat in FEATURES:
        percentiles[feat] = {
            "q25": float(test_df[feat].quantile(0.25)),
            "q75": float(test_df[feat].quantile(0.75)),
            "mean": float(test_df[feat].mean()),
        }
    return percentiles


def perturb_instance(row: pd.Series, features_to_perturb: list[str],
                     percentiles: dict, direction: str = "auto") -> pd.Series:
    """
    Perturb top-k features to Q25 or Q75.

    direction: 'up' (push to Q75), 'down' (push to Q25), 'auto' (opposite of current)
    """
    perturbed = row.copy()
    for feat in features_to_perturb:
        q25 = percentiles[feat]["q25"]
        q75 = percentiles[feat]["q75"]
        current = row[feat]
        if direction == "up":
            perturbed[feat] = q75
        elif direction == "down":
            perturbed[feat] = q25
        else:  # auto: push to the far quartile
            if current < percentiles[feat]["mean"]:
                perturbed[feat] = q75
            else:
                perturbed[feat] = q25
    return perturbed


