def _explain_block_shap(shap_dict: dict, base_val: float) -> str:
    """Paper 1's _explain_block for SHAP — sorted by magnitude, +/-.3f format."""
    ordered = sorted(shap_dict.items(), key=lambda kv: abs(kv[1]), reverse=True)
    lines = [f"{k}: {v:+.3f}" for k, v in ordered]
    block = ["SHAP values:"] + lines + [f"The expected/base value for SHAP: {base_val:.3f}"]
    return "\n".join(block) + "\n"


