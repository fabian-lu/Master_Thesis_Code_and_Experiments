class GPTClient:
    """Thin wrapper around Azure OpenAI GPT-4o (sync). Returns plain text strings."""

    def call(self, system_msg: str, human_msg: str,
             temperature: float = 0.0, max_tokens: int = 1024) -> str:
        return _call_gpt_sync(system_msg, human_msg, temperature, max_tokens)["text"]

    def call_with_retry(self, system_msg: str, human_msg: str,
                        temperature: float = 0.0, max_tokens: int = 1024,
                        retries: int = 3, wait: float = 5.0) -> str:
        for attempt in range(retries):
            try:
                return self.call(system_msg, human_msg, temperature, max_tokens)
            except Exception as e:
                if attempt < retries - 1:
                    print(f"  Retry {attempt+1}/{retries} after error: {e}")
                    time.sleep(wait)
                else:
                    raise


