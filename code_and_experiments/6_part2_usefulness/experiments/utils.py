PCT_ERROR_BINS = {
    "small": (0.0, 5.0),
    "medium": (5.0, 15.0),
    "large": (15.0, 30.0),
    "very_large": (30.0, float("inf")),
}


def pct_error_to_bucket(pct_error: float) -> str:
    for label, (lo, hi) in PCT_ERROR_BINS.items():
        if lo <= abs(pct_error) < hi:
            return label
    return "very_large"


